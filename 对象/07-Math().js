// 绝对值
console.log("绝对值");
console.log(Math.abs(-2));

// 向上取整
console.log("向上取整");
console.log(Math.ceil(1.4));

// 向下取整
console.log("向下取整");
console.log(Math.floor(1.7));

// 四舍五入
console.log("四舍五入");
console.log(Math.round(1.7));
console.log(Math.round(1.4));

// 生成0-1的随机数 不包括0 ， 1
// 生成x-y之间的随机数 math.random()*(y-x)+x
console.log("生成0-1的随机数");
console.log(Math.random());

//最大值
console.log("最大值");
console.log(Math.max(10,20,30,90,80)); 

// 最小值
console.log("最小值");
console.log(Math.min(10,20,30,90,80)); 